context("Basic calculations")

bf <- function(data_model, h1_model, h0_model) {
  lik <- do.call(bayesplay::likelihood, data_model)
  h1 <- do.call(bayesplay::prior, h1_model)
  h0 <- do.call(bayesplay::prior, h0_model)
  m1 <- integral(lik * h1)
  m0 <- integral(lik * h0)
  unclass(m1 / m0)
}

